#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn exposes_full_range_and_unknown_code() {
        assert_eq!(KeyCode::Unknown.value(), -1);
        assert_eq!(KeyCode::Home.value(), 1);
        assert_eq!(KeyCode::Wlan.value(), 2844);
        assert_eq!(KeyCode::Button9.value(), 3109);
    }
}
